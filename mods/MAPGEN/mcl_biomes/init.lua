			for _, pos in ipairs(gennotify["decoration#"..deco_id_chorus_plant] or {}) do
				local realpos = { x = pos.x, y = pos.y + 1, z = pos.z }
				mcl_end.grow_chorus_plant(realpos)
			end
		end)
	end

